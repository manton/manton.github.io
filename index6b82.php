<p>I like what ESPN is doing in the sidebar <a href="http://espn.go.com/nba/scoreboard">on their NBA scores page</a>. It&#8217;s a timeline of both tweets and short ESPN posts, integrated together with a clean design that fits the rest of the site.</p>
<p>This timeline is a great use of microblogging. The short posts aren&#8217;t limited to tweet-length — they&#8217;re often around 200 characters instead — so they can feel complete and informative while still being concise. <a href="http://www.manton.org/2014/09/defining-a-microblog-post.html">I&#8217;ve suggested 280 characters</a> as a guideline for microblogs, and having the extra characters to work with really makes a nice difference.</p>
<p>I took an example screenshot from ESPN and included it to the right of this post. The first two posts are these special ESPN microblog posts, and the third is a tweet. I don&#8217;t know what CMS-like system is driving this, but you can imagine using WordPress post formats, custom fields, or categories to achieve something similar.</p>
